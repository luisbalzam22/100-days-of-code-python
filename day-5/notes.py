# sum()
sum([1,2,3]) # 6

# max()
max([1,3,2]) # 3

# min()
min([3,1,2]) # 1

# range(inclusive_start, exclusive_end, step)
range(2, 101, 2) # 2,4,6,8,...100

# random.shuffle()
random.shuffle([1,2,3,4,5,6]) # [5,3,4,6,1,2] / [4,1,5,6,3,2] / [6,1,2,4,3,5] / etc.

